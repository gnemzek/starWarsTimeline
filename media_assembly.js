function checkBoxFilter() {
  let checkedArray = [];

  mediaCollection.forEach((element) => {
    if (document.getElementById("not-consumed").checked) {
      if (!element.consumed) {
        //console.log(element.title + " - Not consumed");

        checkedArray.push(element);
      }
    }

    if (document.getElementById("consumed").checked) {
      if (element.consumed) {
        //console.log(element.title + " - Consumed");

        checkedArray.push(element);
      }
    }

    if (document.getElementById("comics").checked) {
      if (element.type === "comic") {
        // console.log(element.title + " - Comics");

        checkedArray.push(element);
      }
    }

    if (document.getElementById("movies").checked) {
      if (element.type === "movie") {
        // console.log(element.title + " - Movies");

        checkedArray.push(element);
      }
    }

    if (document.getElementById("tv-shows").checked) {
      if (element.type === "tv") {
        //console.log(element.title + " - TV-Shows");

        checkedArray.push(element);
      }
    }

    if (document.getElementById("books").checked) {
      if (element.type === "book") {
        // console.log(element.title + " - Books");

        checkedArray.push(element);
      }
    }
  });

  // sort by order
  checkedArray.sort((a, b) => (a.order > b.order ? 1 : -1));

  // remove duplicates
  let unique_checkedArray = [...new Set(checkedArray)];

  addMedia(unique_checkedArray);
}

function selectFilter() {
  let selectArray = [];

  var select_consumed = document.getElementById("consumed-select").value;
  var select_type = document.getElementById("type").value;

  console.log(select_consumed);
  if (select_type != "All") {
    mediaCollection.forEach((element) => {
      if (
        element.consumed.toString() === select_consumed &&
        element.type === select_type
      ) {
        selectArray.push(element);
      }
    });
  } else {
    mediaCollection.forEach((element) => {
      if (element.consumed.toString() === select_consumed) {
        selectArray.push(element);
      }
    });
  }

  addMedia(selectArray);
}
